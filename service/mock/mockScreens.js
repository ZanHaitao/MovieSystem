function getRandom(min, max) {
    return Math.floor(Math.random() * (max - min + 1) + min);
}
// 1836 - 2994

const CinemaScreensService = require('../service/CinemaScreensService');

async function setData() {
    let CinemaId = 1863;
    while (CinemaId < 2995) {

        const screensNum = getRandom(4, 6);
        let screensData = [];
        for (let i = 0; i < screensNum; i++) {
            const data = ScreenArr[getRandom(0, ScreenArr.length - 1)]
            if (!screensData.includes(data)) {
                screensData.push(data);
            } else {
                i--;
            }
        }
        for (const item of screensData) {
            await CinemaScreensService.addCinemaScreens({
                CinemaId:CinemaId,
                ScreenId:item
            })
        }

        CinemaId++;
    }
    console.log('数据写入成功');
}
setData();

